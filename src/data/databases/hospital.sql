PRAGMA foreign_keys = ON;

-- DDL

CREATE TABLE IF NOT EXISTS province_names (
  province_id   TEXT PRIMARY KEY,
  province_name TEXT NOT NULL,
  country       TEXT NOT NULL DEFAULT 'Canada'
);

CREATE TABLE IF NOT EXISTS patients (
  patient_id   INTEGER PRIMARY KEY,
  first_name   TEXT    NOT NULL,
  last_name    TEXT    NOT NULL,
  birth_date   DATE,
  gender       TEXT    CHECK(gender IN ('M','F','Other')),
  weight       REAL,
  height       REAL,
  allergies    TEXT,
  city         TEXT,
  province_id  TEXT REFERENCES province_names(province_id)
);

CREATE TABLE IF NOT EXISTS departments (
  department_id   INTEGER PRIMARY KEY,
  department_name TEXT    NOT NULL,
  head_doctor_id  INTEGER REFERENCES doctors(doctor_id)
);

CREATE TABLE IF NOT EXISTS doctors (
  doctor_id     INTEGER PRIMARY KEY,
  first_name    TEXT    NOT NULL,
  last_name     TEXT    NOT NULL,
  specialty     TEXT,
  department_id INTEGER REFERENCES departments(department_id)
);

CREATE TABLE IF NOT EXISTS admissions (
  admission_id        INTEGER PRIMARY KEY,
  patient_id          INTEGER NOT NULL REFERENCES patients(patient_id),
  admission_date      DATE    NOT NULL,
  discharge_date      DATE,
  diagnosis           TEXT,
  attending_doctor_id INTEGER REFERENCES doctors(doctor_id)
);

CREATE TABLE IF NOT EXISTS diagnoses (
  diagnosis_id  INTEGER PRIMARY KEY,
  admission_id  INTEGER NOT NULL REFERENCES admissions(admission_id),
  icd_code      TEXT,
  description   TEXT,
  severity      TEXT CHECK(severity IN ('Mild','Moderate','Severe','Critical'))
);

CREATE TABLE IF NOT EXISTS medications (
  medication_id  INTEGER PRIMARY KEY,
  admission_id   INTEGER NOT NULL REFERENCES admissions(admission_id),
  drug_name      TEXT    NOT NULL,
  dosage         TEXT,
  start_date     DATE,
  end_date       DATE
);

